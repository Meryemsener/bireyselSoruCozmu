package nisan1;

public class CharAt {
    public static void main(String[] args) {
        // charAt(istenenIndex) method'u istedigimiz index'deki karakteri bize verir

        String motto="her sey cok guzel oluyor";
        System.out.println(motto.charAt(6));//y
        System.out.println(motto.charAt(10));//k
        System.out.println(motto.charAt(12));//g
        System.out.println(motto.charAt(9));//o

    }
}
